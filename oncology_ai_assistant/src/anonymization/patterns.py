"""
=============================================================================
PATTERNS.PY - Regex-паттерны для обнаружения персональных данных (PII)
=============================================================================
Модуль содержит компилируемые регулярные выражения для поиска и замены
персональной информации в медицинских документах.

Категории PII:
- ФИО (фамилия, имя, отчество)
- Паспортные данные
- Номер полиса ОМС/ДМС
- СНИЛС
- Дата рождения
- Адрес
- Телефон
- Email
- Номер медицинской карты
=============================================================================
"""

import re
from typing import Dict, Pattern
from dataclasses import dataclass


@dataclass
class PatternConfig:
    """Конфигурация паттерна для анонимизации."""
    name: str
    pattern: Pattern
    placeholder: str
    description: str


# -----------------------------------------------------------------------------
# Словари русских фамилий, имён и отчеств для более точного обнаружения
# -----------------------------------------------------------------------------
COMMON_SURNAME_ENDINGS = [
    'ов', 'ова', 'ев', 'ева', 'ин', 'ина', 'ын', 'ына',
    'ский', 'ская', 'цкий', 'цкая', 'ой', 'ая',
    'ий', 'ая', 'ый', 'яя', 'енко', 'ко', 'ич', 'на'
]

COMMON_MALE_NAMES = [
    'Александр', 'Алексей', 'Анатолий', 'Андрей', 'Антон', 'Артём', 'Борис',
    'Вадим', 'Валентин', 'Валерий', 'Василий', 'Виктор', 'Виталий', 'Владимир',
    'Владислав', 'Всеволод', 'Вячеслав', 'Геннадий', 'Георгий', 'Герман',
    'Григорий', 'Денис', 'Дмитрий', 'Евгений', 'Егор', 'Иван', 'Игорь',
    'Илья', 'Константин', 'Кирилл', 'Максим', 'Михаил', 'Николай', 'Олег',
    'Павел', 'Пётр', 'Роман', 'Сергей', 'Станислав', 'Степан', 'Тимофей',
    'Фёдор', 'Юрий', 'Ярослав'
]

COMMON_FEMALE_NAMES = [
    'Александра', 'Алёна', 'Алина', 'Алла', 'Анастасия', 'Анна', 'Валентина',
    'Валерия', 'Варвара', 'Вера', 'Вероника', 'Виктория', 'Галина', 'Дарья',
    'Евгения', 'Екатерина', 'Елена', 'Жанна', 'Зинаида', 'Ирина', 'Ксения',
    'Лариса', 'Лидия', 'Любовь', 'Людмила', 'Маргарита', 'Марина', 'Мария',
    'Надежда', 'Наталья', 'Нина', 'Ольга', 'Раиса', 'Светлана', 'София',
    'Тамара', 'Татьяна', 'Юлия'
]

PATRONYMIC_ENDINGS = ['ович', 'евич', 'ич', 'овна', 'евна', 'ична', 'инична']


# -----------------------------------------------------------------------------
# PATTERNS - основной словарь паттернов
# -----------------------------------------------------------------------------
PATTERNS: Dict[str, PatternConfig] = {
    # -------------------------------------------------------------------------
    # ФИО
    # -------------------------------------------------------------------------
    'full_name': PatternConfig(
        name='full_name',
        pattern=re.compile(
            r'\b(' + '|'.join(COMMON_MALE_NAMES + COMMON_FEMALE_NAMES) + r')\s+'
            r'([А-ЯЁ][а-яё]+(?:ов|ев|ин|ын|ский|цкий|енко|ко|ой|ий|ый))\s+'
            r'([А-ЯЁ][а-яё]+(?:ович|евич|ич|овна|евна|ична|инична))?\b',
            re.IGNORECASE
        ),
        placeholder='[ФИО]',
        description='Полное ФИО пациента (Фамилия Имя Отчество)'
    ),
    
    # Альтернативный паттерн для ФИО в формате "Фамилия И.О."
    'full_name_initials': PatternConfig(
        name='full_name_initials',
        pattern=re.compile(
            r'\b([А-ЯЁ][а-яё]+(?:ов|ев|ин|ын|ский|цкий|енко|ко|ой|ий|ый))\s+'
            r'([А-ЯЁ])\.\s*([А-ЯЁ])\.?\b',
            re.IGNORECASE
        ),
        placeholder='[ФИО]',
        description='ФИО в сокращённой форме (Фамилия И.О.)'
    ),
    
    # -------------------------------------------------------------------------
    # Паспортные данные
    # -------------------------------------------------------------------------
    'passport': PatternConfig(
        name='passport',
        pattern=re.compile(
            r'\b(?:паспорт)?\s*'
            r'(\d{4})\s*[№#-]?\s*(\d{6})\b|'
            r'\b(\d{4})\s+(\d{6})\b',
            re.IGNORECASE
        ),
        placeholder='[ПАСПОРТ]',
        description='Серия и номер паспорта РФ'
    ),
    
    'passport_issue': PatternConfig(
        name='passport_issue',
        pattern=re.compile(
            r'\bвыдан\s+'
            r'(.{20,100}?)\s*'
            r'(?:\d{2}\.\d{2}\.\d{4})?\b',
            re.IGNORECASE
        ),
        placeholder='[ПАСПОРТ_ВЫДАН]',
        description='Кем и когда выдан паспорт'
    ),
    
    # -------------------------------------------------------------------------
    # Полис ОМС/ДМС
    # -------------------------------------------------------------------------
    'policy_oms': PatternConfig(
        name='policy_oms',
        pattern=re.compile(
            r'\b(?:полис\s*(?:ОМС|ДМС|медицинский))?\s*'
            r'№?\s*'
            r'(\d{16}|\d{11})\b',
            re.IGNORECASE
        ),
        placeholder='[ПОЛИС]',
        description='Номер полиса ОМС/ДМС (11 или 16 цифр)'
    ),
    
    # -------------------------------------------------------------------------
    # СНИЛС
    # -------------------------------------------------------------------------
    'snils': PatternConfig(
        name='snils',
        pattern=re.compile(
            r'\b(?:СНИЛС)?\s*'
            r'(\d{3})[-\s]?(\d{3})[-\s]?(\d{3})[-\s]?(\d{2})\b',
            re.IGNORECASE
        ),
        placeholder='[СНИЛС]',
        description='СНИЛС (формат: 123-456-789 12)'
    ),
    
    # -------------------------------------------------------------------------
    # Дата рождения
    # -------------------------------------------------------------------------
    'birth_date': PatternConfig(
        name='birth_date',
        pattern=re.compile(
            r'\b(?:дата\s*рождения?|род\.?|р\.?)\s*'
            r'(\d{1,2})[./-](\d{1,2})[./-](\d{4})\b|'
            r'\b(\d{1,2})[./-](\d{1,2})[./-](\d{4})\s*'
            r'(?:года?|г\.?р\.?)\b',
            re.IGNORECASE
        ),
        placeholder='[ДАТА_РОЖДЕНИЯ]',
        description='Дата рождения пациента'
    ),
    
    # -------------------------------------------------------------------------
    # Адрес
    # -------------------------------------------------------------------------
    'address': PatternConfig(
        name='address',
        pattern=re.compile(
            r'\b(?:адрес|проживающ(?:ий|ая)|место\s*жительства)\s*'
            r'[:\-]?\s*'
            r'([А-ЯЁ][а-яё]+(?:ская|ский|ское)?\s+)?'
            r'([А-ЯЁ][а-яё]+(?:ово|ево|ино|ыно|град|ск|ец)?(?:\s+р-?н)?)\s+'
            r'([А-ЯЁ][а-яё]+(?:ая|ий|ский|ская|ское))?\s*'
            r'(?:ул\.?|улица|просп\.?|проспект|пер\.?|переулок|бульвар|площадь|шоссе)\s+'
            r'([А-ЯЁ][а-яё]+)\s*'
            r'(?:д\.?\s*(\d+))?'
            r'(?:,\s*кв\.?\s*(\d+))?',
            re.IGNORECASE
        ),
        placeholder='[АДРЕС]',
        description='Адрес проживания пациента'
    ),
    
    # Упрощённый паттерн для адреса
    'address_short': PatternConfig(
        name='address_short',
        pattern=re.compile(
            r'\b(?:ул\.?|улица|пр-?\.?|проспект|пер\.?|переулок)\s+'
            r'[А-ЯЁ][а-яё]+\s*'
            r'(?:д\.?\s*\d+)?'
            r'(?:,\s*(?:кв\.?|офис)\s*\d+)?',
            re.IGNORECASE
        ),
        placeholder='[АДРЕС]',
        description='Краткий адрес (улица, дом)'
    ),
    
    # -------------------------------------------------------------------------
    # Телефон
    # -------------------------------------------------------------------------
    'phone': PatternConfig(
        name='phone',
        pattern=re.compile(
            r'\b(?:тел\.?|телефон|моб\.?|mobile)?\s*'
            r'(?:\+7|8)\s*'
            r'\(?\d{3}\)?\s*'
            r'\d{3}[-\s]?\d{2}[-\s]?\d{2}\b',
            re.IGNORECASE
        ),
        placeholder='[ТЕЛЕФОН]',
        description='Номер телефона (российский формат)'
    ),
    
    # -------------------------------------------------------------------------
    # Email
    # -------------------------------------------------------------------------
    'email': PatternConfig(
        name='email',
        pattern=re.compile(
            r'\b[a-zA-Z0-9._%+-]+'
            r'@[a-zA-Z0-9.-]+'
            r'\.[a-zA-Z]{2,}\b',
            re.IGNORECASE
        ),
        placeholder='[EMAIL]',
        description='Адрес электронной почты'
    ),
    
    # -------------------------------------------------------------------------
    # Номер медицинской карты
    # -------------------------------------------------------------------------
    'medical_record': PatternConfig(
        name='medical_record',
        pattern=re.compile(
            r'\b(?:мед\.?\s*карта|амбулаторная\s*карта|история\s*болезни|№\s*карты)\s*'
            r'[:\-]?\s*'
            r'№?\s*'
            r'(\d{5,10})\b',
            re.IGNORECASE
        ),
        placeholder='[МЕД_КАРТА]',
        description='Номер медицинской карты'
    ),
    
    # -------------------------------------------------------------------------
    # Номер истории болезни
    # -------------------------------------------------------------------------
    'case_history': PatternConfig(
        name='case_history',
        pattern=re.compile(
            r'\b(?:история\s*болезни|стационарный\s*больной)\s*'
            r'[:\-]?\s*'
            r'№?\s*'
            r'(\d{4,8})\b',
            re.IGNORECASE
        ),
        placeholder='[ИСТОРИЯ_БОЛЕЗНИ]',
        description='Номер истории болезни'
    ),
    
    # -------------------------------------------------------------------------
    # ИНН
    # -------------------------------------------------------------------------
    'inn': PatternConfig(
        name='inn',
        pattern=re.compile(
            r'\b(?:ИНН)?\s*'
            r'(\d{10}|\d{12})\b',
            re.IGNORECASE
        ),
        placeholder='[ИНН]',
        description='Идентификационный номер налогоплательщика'
    ),
}


# -----------------------------------------------------------------------------
# Функции для работы с паттернами
# -----------------------------------------------------------------------------
def get_pattern(name: str) -> PatternConfig:
    """
    Получить паттерн по имени.
    
    Args:
        name: Имя паттерна
        
    Returns:
        PatternConfig или None если паттерн не найден
        
    Raises:
        KeyError: Если паттерн не найден
    """
    if name not in PATTERNS:
        raise KeyError(f"Паттерн '{name}' не найден. Доступные: {list(PATTERNS.keys())}")
    return PATTERNS[name]


def get_all_patterns() -> Dict[str, PatternConfig]:
    """
    Получить все доступные паттерны.
    
    Returns:
        Словарь всех паттернов
    """
    return PATTERNS.copy()


def get_pattern_names() -> list:
    """
    Получить список имён всех паттернов.
    
    Returns:
        Список имён паттернов
    """
    return list(PATTERNS.keys())
