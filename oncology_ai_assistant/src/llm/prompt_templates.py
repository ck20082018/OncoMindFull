@dataclass
class PromptTemplate:
    """Шаблон промпта с подстановкой переменных."""
    name: str
    template: str
    description: str
    
    def render(self, **kwargs) -> str:
        """
        Подставить переменные в шаблон.
        
        Args:
            **kwargs: Переменные для подстановки.
            
        Returns:
            Готовый промпт.
        """
        return Template(self.template).safe_substitute(**kwargs)


