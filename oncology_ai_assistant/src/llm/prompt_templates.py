def get_system_prompt(role: str) -> str:
    """
    Получить системный промпт для роли.
    
    Args:
        role: 'doctor', 'patient', или 'extraction'.
        
    Returns:
        Текст системного промпта.
        
    Raises:
        ValueError: Если роль не найдена.
    """
    prompts = {
        'doctor': SYSTEM_PROMPT_DOCTOR,
        'patient': SYSTEM_PROMPT_PATIENT,
        'extraction': SYSTEM_PROMPT_EXTRACTION
    }
    
    if role not in prompts:
        raise ValueError(
            f"Роль '{role}' не найдена. Доступные: {list(prompts.keys())}"
        )
    
    return prompts[role]


def get_template(name: str) -> PromptTemplate:
    """
    Получить шаблон по имени.
    
    Args:
        name: Имя шаблона.
        
    Returns:
        PromptTemplate.
        
    Raises:
        KeyError: Если шаблон не найден.
    """
    if name not in PROMPT_TEMPLATES:
        raise KeyError(
            f"Шаблон '{name}' не найден. Доступные: {list(PROMPT_TEMPLATES.keys())}"
        )
    return PROMPT_TEMPLATES[name]


def create_doctor_prompt(
    patient_id: str,
    clinical_data: str,
    examination_results: str,
    current_treatment: str,
    guideline_excerpts: str
) -> str:
    """
    Создать промпт для проверки лечения врачом.
    
    Args:
        patient_id: Идентификатор пациента.
        clinical_data: Клинические данные.
        examination_results: Результаты обследования.
        current_treatment: Текущее лечение.
        guideline_excerpts: Выдержки из рекомендаций.
        
    Returns:
        Готовый промпт.
    """
    template = get_template('check_treatment')
    return template.render(
        PATIENT_ID=patient_id,
        CLINICAL_DATA=clinical_data,
        EXAMINATION_RESULTS=examination_results,
        CURRENT_TREATMENT=current_treatment,
        GUIDELINE_EXCERPTS=guideline_excerpts
    )


def create_patient_prompt(
    patient_data: str,
    diagnosis: str,
    treatment_plan: str
) -> str:
    """
    Создать промпт для объяснения пациенту.
    
    Args:
        patient_data: Данные пациента.
        diagnosis: Диагноз.
        treatment_plan: План лечения.
        
    Returns:
        Готовый промпт.
    """
    template = get_template('explain_to_patient')
    return template.render(
        PATIENT_DATA=patient_data,
        DIAGNOSIS=diagnosis,
        TREATMENT_PLAN=treatment_plan
    )


def create_extraction_prompt(document_text: str) -> str:
    """
    Создать промпт для извлечения данных.
    
    Args:
        document_text: Текст документа.
        
    Returns:
        Готовый промпт.
    """
    template = get_template('extract_data')
    return template.render(DOCUMENT_TEXT=document_text)


